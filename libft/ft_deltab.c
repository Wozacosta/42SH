#include "libft.h"

void	ft_del_tab(char **tb)
{
	int	i;

	i = 0;
	if (tb != NULL)
	{
		while (tb != NULL)
		{
			free(tb[i]);
			i++;
		}
		free(tb[i]);
		free(tb);
	}
}
