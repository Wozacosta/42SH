#include "libft.h"

size_t		ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	d_len;
	size_t	s_len;

	d_len = ft_strlen(dst);
	s_len = ft_strlen(src);
	if (d_len > size)
		return (s_len + size);
	ft_strncat(dst, src, (size - d_len));
	return (d_len + s_len);
}
